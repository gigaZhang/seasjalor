package com.documentformwork.model;

/**
 * 表示系统管理模块
 * 
 * @author style
 * 
 */
public class Module {
	private String name;
	private String url;
	private String icon;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getIcon() {
		return icon;
	}

	public void setIcon(String icon) {
		this.icon = icon;
	}
}
